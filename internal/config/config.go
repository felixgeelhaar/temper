package config

import (
	"os"
	"strconv"
)

// Environment-variable helpers shared by config.LoadLocalConfig and tests.
// The legacy Config struct + Load() function were removed because they
// reflected an older multi-service architecture (Postgres, RabbitMQ,
// session secrets) that no longer ships. Local daemon configuration
// lives in LocalConfig (see local.go).

func getEnv(key, defaultValue string) string {
	if value := os.Getenv(key); value != "" {
		return value
	}
	return defaultValue
}

func getEnvInt(key string, defaultValue int) int {
	if value := os.Getenv(key); value != "" {
		if i, err := strconv.Atoi(value); err == nil {
			return i
		}
	}
	return defaultValue
}

func getEnvFloat(key string, defaultValue float64) float64 {
	if value := os.Getenv(key); value != "" {
		if f, err := strconv.ParseFloat(value, 64); err == nil {
			return f
		}
	}
	return defaultValue
}

func getEnvBool(key string, defaultValue bool) bool {
	if value := os.Getenv(key); value != "" {
		if b, err := strconv.ParseBool(value); err == nil {
			return b
		}
	}
	return defaultValue
}
